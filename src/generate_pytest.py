# Main
# ──────────────────────────────────────────────

def main(spec_path: str, output_dir: str = "tests/generated") -> None:
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    parser = SpecParser(spec_path)
    all_cases = parser.test_cases()

    # Group by tag
    by_tag: dict[str, list[TestCase]] = defaultdict(list)
    for tc in all_cases:
        by_tag[tc.tag].append(tc)

    # Write conftest
    (out / "conftest.py").write_text(CONFTEST)
    (out / "__init__.py").write_text("")

    # Write one file per tag
    for tag, cases in by_tag.items():
        fname = out / f"test_{tag}.py"
        fname.write_text(generate_test_file(tag, cases))

    # Print summary
    print("\n" + "═" * 60)
    print("  PYTEST GENERATOR — Output Summary")
    print("═" * 60)
    print(f"  Spec           : {spec_path}")
    print(f"  Output dir     : {output_dir}/")
    print(f"  Total tests    : {len(all_cases)}")
    print()
    for tag, cases in sorted(by_tag.items()):
        print(f"  test_{tag}.py  →  {len(cases)} tests")
    print()
    s = parser.summary()
    print("  By scenario:")
    for scenario, count in sorted(s["by_scenario"].items()):
        bar = "█" * (count // 2)
        print(f"    {scenario:<20} {count:>3}  {bar}")
    print("═" * 60 + "\n")
    print("  Run with:")
    print(f"    pytest {output_dir}/ -v")
    print(f"    pytest {output_dir}/ -v --tb=short -q")
    print()


if __name__ == "__main__":
    spec  = sys.argv[1] if len(sys.argv) > 1 else "specs/petstore.yaml"
    outd  = sys.argv[2] if len(sys.argv) > 2 else "tests/generated"
    main(spec, outd)
