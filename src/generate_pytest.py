INDENT = "    "

# Sample values for fake data by format / type
FAKE_VALUES = {
    "int64":    10,
    "int32":    5,
    "float":    1.5,
    "double":   3.14,
    "string":   '"test_value"',
    "boolean":  "True",
    "email":    '"test@example.com"',
    "date-time": '"2026-05-22T15:45:00Z"',
    "uri":      '"https://example.com/photo.jpg"',
}

WRONG_TYPE_VALUES = {
    "integer": '"not_an_integer"',
    "number":  '"not_a_number"',
    "boolean": '"not_a_bool"',
}


def fake_value(schema: dict) -> str:
    fmt = schema.get("format", "")
    typ = schema.get("type", "string")
    if schema.get("enum"):
        return f'"{schema["enum"][0]}"'
    if fmt in FAKE_VALUES:
        return str(FAKE_VALUES[fmt])
    return str(FAKE_VALUES.get(typ, '"test_value"'))


def build_path(path: str, path_params: list[dict]) -> str:
    """Replace {param} placeholders with fixture references."""
    result = path
    for p in path_params:
        name = p["name"]
        schema = p.get("schema", {})
        val = fake_value(schema)
        result = result.replace(f"{{{name}}}", str(val).strip('"'))
    return result


def build_body(schema: dict, scenario: str, missing_field: str | None = None) -> str:
    """Generate a Python dict literal for the request body."""
    if not schema:
        return "None"
    props = schema.get("properties", {})
    required = schema.get("required", [])
    lines = ["{"]
    for prop, pschema in props.items():
        if scenario == "missing_required" and prop == missing_field:
            continue  # deliberately omit
        val = fake_value(pschema)
        # handle arrays
        if pschema.get("type") == "array":
            item_schema = pschema.get("items", {})
            if item_schema.get("type") == "object":
                val = "[]"
            else:
                val = f'[{fake_value(item_schema)}]'
        lines.append(f'        "{prop}": {val},')
    lines.append("    }")
    return "\n".join(lines)


def build_query_params(query_params: list[dict], scenario: str) -> str:
    if not query_params:
        return "None"
    lines = ["{"]
    for p in query_params:
        name = p["name"]
        schema = p.get("schema", {})
        if scenario == "invalid_enum" and schema.get("enum"):
            val = '"__invalid_enum_value__"'
        else:
            val = fake_value(schema)
        lines.append(f'        "{name}": {val},')
    lines.append("    }")
    return "\n".join(lines)


