# Per-test function generator
# ──────────────────────────────────────────────

def render_test_function(tc: TestCase) -> str:
    """Return the source code for a single pytest test function."""
    method = tc.method.lower()
    url_path = build_path(tc.path, tc.path_params)
    body = build_body(
        tc.request_body_schema or {},
        tc.scenario,
    )
    params = build_query_params(tc.query_params, tc.scenario)

    # auth fixture
    if tc.scenario == "no_auth":
        auth_arg = "no_auth_headers"
    elif tc.security:
        auth_arg = "auth_headers"
    else:
        auth_arg = "auth_headers"

    expected = tc.expected_status

    lines = [
        f"def {tc.test_name}(base_url, {auth_arg}):",
        f'    """',
        f'    [{tc.tag.upper()}] {tc.summary}',
        f'    Scenario : {tc.scenario}',
        f'    Expected : HTTP {expected}',
        f'    """',
        f'    url = f"{{base_url}}{url_path}"',
    ]

    if body != "None":
        lines.append(f"    payload = {body}")
    if params != "None":
        lines.append(f"    params  = {params}")

    # Build the requests call
    call_args = [f"url", f"headers={auth_arg}"]
    if body != "None":
        call_args.append("json=payload")
    if params != "None":
        call_args.append("params=params")

    lines.append(f"    response = requests.{method}(")
    for i, arg in enumerate(call_args):
        sep = "," if i < len(call_args) - 1 else ""
        lines.append(f"        {arg}{sep}")
    lines.append(f"    )")
    lines.append(f"")
    lines.append(f"    assert response.status_code == {expected}, (")
    lines.append(f'        f"Expected {expected}, got {{response.status_code}}\\n"')
    lines.append(f'        f"Body: {{response.text[:200]}}"')
    lines.append(f"    )")

    # Schema validation on 2xx happy path
    if tc.scenario == "happy_path" and tc.request_body_schema:
        lines += [
            "",
            "    # Basic schema validation",
            "    if response.status_code < 300:",
            "        data = response.json()",
            "        assert isinstance(data, (dict, list)), \\",
            '            "Response should be a JSON object or array"',
        ]

    return "\n".join(f"{INDENT}{line}" if i > 0 else line
                     for i, line in enumerate(lines))


